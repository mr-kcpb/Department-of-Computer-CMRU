                                    และต้องมาเพิ่ม (แอดมือ) ในระบบได้ที่</p>
                                <p>สำนักทะเบียนฯ ในช่วงเพิ่ม-ถอน ตอนเปิดภาคเรียน และต้องให้ผู้สอนอนุมัติในระบบ</p>
                                <p>• ผู้ที่ไม่ได้ยื่นขอเปิดจะเห็นรายวิชาขอเปิด แต่ไม่สามารถคลิกลงทะเบียนได้
                                    จะต้องติดต่ออ.ผู้สอนเซ็นต์อนุญาตให้เรียน/เปิดเพิ่มจำนวน</p>
                                <p>แล้วยื่นเอกสาร/แจ้งไปยังสำนักทะเบียนฯ จึงจะให้ลงทะเบียนเรียนได้</p>
                                <p>11. การยื่นขอเปิดรายวิชาการศึกษาอิสระฯ</p>
