                                <p> - ขออนุเคราะห์นักศึกษามายื่นขอเรียนร่วม/เรียนเกินด้วยตนเองเท่านั้น
                                    เว้นแต่จะมีเหตุจำเป็นอันสุดวิสัย อยู่ต่างจังหวัด เนื่องจาก ตามระเบียบมหาวิทยาลัย
                                    นักศึกษาต้องลงทะเบียนและดำเนินการด้วยตนเอง เพื่อผลประโยชน์ของนักศึกษาเป็นสำคัญ</p>
                                <p>- อาจารย์ที่ปรึกษาและภาควิชาจะได้ทราบข้อมูลนักศึกษา แจ้งข้อมูลที่เป็นประโยชน์
                                    ที่ทำให้จบการศึกษาได้โดยรวดเร็ว ทันรับปริญญา และอนุมัติจบได้ภายในระยะเวลากำหนด</p>
                                <p>- นศ.ภาคพิเศษที่มาเรียนร่วมกับภาคปกติ จะต้องเรียน/ทำกิจกรรมเหมือนกับภาคปกติ
                                    ทุกอย่าง</p>
                                <p>- ขอความอนุเคราะห์สละเวลามาดำเนินการภายในระยะเวลากำหนด</p>
                                <p>- นักศึกษาภาคพิเศษ สามารถมาในตอนเย็นวันจันทร์-ศุกร์
                                    และเสาร์-อาทิตย์ที่ไม่ตรงกับทำงานหรือติดราชการได้</p>
