                                <h4>การปฏิบัติของผู้เข้าสอบ</h4>
                                <p>ข้อห้ามสำหรับผู้เข้าสอบ มีดังต่อไปนี้</p>
                                <p>(๑) ห้ามใช้เครื่องมือสื่อสารในห้องสอบ</p>
                                <p>(๒) ห้ามส่งเสียงดัง ทำความรำคาญ
                                    หรือแสดงกิริยามารยาทไม่เหมาะสมในห้องสอบหรือบริเวณใกล้เคียงห้องสอบ</p>
                                <p>(๓) ห้ามเข้าห้องสอบหลังจากเริ่มสอบวิชานั้นแล้ว ๑๕ นาที</p>
                                <p>(๔) ห้ามออกจากห้องสอบก่อน ๓๐ นาที นับจากเริ่มสอบวิชานั้น</p>
                                <p>(๕) ห้ามเข้าห้องสอบก่อนได้รับอนุญาตจากผู้กำกับการสอบ</p>
                                <p>(๖) ห้ามผู้เข้าสอบติดต่อหรือพูดจากับผู้หนึ่งผู้ใดในเวลาสอบ
                                    เมื่อมีกิจจำเป็นต้องการสิ่งหนึ่งสิ่งใดในระหว่างสอบหรือมีข้อสงสัยให้แจ้งต่อผู้กำกับการสอบ
                                    และให้ผู้กำกับการสอบเป็นผู้พิจารณาอนุญาต</p>
                                <p>(๗) ห้ามผู้เข้าสอบนำสิ่งใด ๆ เข้าห้องสอบ เว้นแต่ปากกา ดินสอ ไม้บรรทัดยางลบ
                                    และสิ่งที่ประกาศให้ทราบล่วงหน้าหรือสิ่งที่ผู้สอนอนุญาตไว้ล่วงหน้าให้นำเข้าห้องสอบเพื่อใช้ในการสอบได้
                                    หรือสิ่งที่ผู้กำกับการสอบอนุญาตให้นำเข้าห้องสอบได้</p>
                                <p>(๘) ห้ามผู้เข้าสอบนำกระดาษคำตอบ กระดาษข้อสอบ กระดาษที่ใช้ในการสอบออกจากห้องสอบ
                                    ยกเว้นในส่วนที่ผู้สอนวิชานั้นกำหนดไว้ให้นำออกจากห้องสอบได้</p>
                                <p>(๙) อื่น ๆ ตามที่มหาวิทยาลัยกำหนด</p>

                                <p>ข้อปฏิบัติสำหรับผู้เข้าสอบ มีดังต่อไปนี้</p>
                                <p>(๑) ให้ผู้เข้าสอบแสดงบัตรประจำตัวนักศึกษาหรือบัตรประจำตัวผู้เข้าสอบ
                                    กรณีไม่มีบัตรประจำตัวนักศึกษาหรือบัตรประจำตัวผู้เข้าสอบมาแสดงในวันสอบ ให้แสดงบัตร
                                    ประจำตัวอื่นซึ่งออกให้โดยหน่วยงานราชการพร้อมกับแสดงหลักฐานอื่นที่มหาวิทยาลัยออกให้
                                    ทั้งนี้ให้อยู่ในดุลพินิจของผู้กำกับการสอบหรือตามที่มหาวิทยาลัยกำหนด</p>
                                <p>(๒) ให้ผู้เข้าสอบนั่งประจำโต๊ะตามที่กำหนดไว้หรือตามที่ผู้กำกับการสอบกำหนด
                                    จะเปลี่ยนที่นั่งก่อนได้รับอนุญาตไม่ได้</p>
                                <p>(๓) ให้ผู้เข้าสอบปฏิบัติตามข้อปฏิบัติที่มหาวิทยาลัยกำหนด
                                    หรือตามที่ผู้กำกับการสอบชี้แจงโดยเคร่งครัด</p>

                                <p>ทุจริตการสอบ ให้มหาวิทยาลัยดำเนินการทางวินัยและพิจารณาโทษตามควรแก่กรณี ดังนี้</p>
                                <p>(๑) สอบตกในรายวิชานั้น และให้พักการเรียน 1 ภาคการศึกษา
                                    ใช้สำหรับนักศึกษาที่ประพฤติผิดเป็นครั้งแรก</p>
                                <p>(๒) สอบตกในรายวิชานั้น และให้พักการเรียน 2 ภาคการศึกษา
                                    ใช้สำหรับนักศึกษาที่เคยถูกลงโทษตาม (๑)</p>
                                <p>(๓) พ้นสภาพการเป็นนักศึกษา ใช้สำหรับนักศึกษาที่เคยถูกลงโทษตาม (๒)</p>

