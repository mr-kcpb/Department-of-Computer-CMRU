                                <h4>การขอเรียนร่วม/เรียนเกิน</h4>

                                <p>การขอเรียนร่วม กับภาคอื่น</p>
                                <p>1) เป็นรายวิชาที่ไม่มีเปิดในภาคการศึกษาที่นศ.สังกัดอยู่</p>
                                <p>2) เป็นรายวิชาที่มีวันและเวลาเรียน/วันและเวลาสอบซ้ำกัน</p>
