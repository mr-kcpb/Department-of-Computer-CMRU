                                <h4>การขอถอนรายวิชา</h4>
                                <p><b>การขอถอนรายวิชา (Withdrawn)</b></p>
                                <p>
                                    นักศึกษาขอถอนรายวิชาที่ได้ลงทะเบียนไปแล้วบางรายวิชาหรือทุกรายวิชาในภาคการศึกษาที่กำลังเรียนเพราะไม่ประสงค์จะเรียน
                                    หรือมีเหตุจำเป็น เช่น เวลาเรียนไม่พอ, เรียนไม่ได้
                                </p>
                                <p><b>ขั้นตอนการขขอถอนรายวิชาเรียน</b></p>
                                <p>1. เลือกรายวิชาที่ต้องการยกเลิก จากเมนูแบบฟอร์มยกเลิกรายวิชาเรียน ใน
                                    ระบบสารสนเทศนักศึกษา</p>
                                <p>2. คลิกเลือกวิชาที่ต้องการยกเลิกรายวิชาเรียนแล้วกด "Submit"
                                    เพื่อส่งข้อมูลการยกเลิกรายวิชาเรียนแล้วพิมพ์แบบฟอร์มคําร้องยกเลิกรายวิชาเรียน</p>
                                <p>3. ขอความเห็นการยกเลิกรายวิชาจากอาจารย์ที่ปรึกษา</p>
                                <p>4. ขอความเห็นของอาจารย์ผู้สอนวิชาที่ขอยกเลิก โดยอาจารย์ผู้สอนตรวจสอบ Section
                                    รหัสวิชาและชื่อวิชาพร้อมเซ็นชื่อกํากับ</p>
                                <p>5. ยื่นคําร้องขอยกเลิกรายวิชาเรียนที่สํานักส่งเสริมวิชาการและงานทะเบียน
                                    พรhอมทั้งแสดงบัตรประจําตัวนักศึกษาหรือบัตรประชาชนภายในเวลาที่มหาวิทยาลัยกําหนด</p>
                                <p>(ดูกําหนดการขอยกเลิกใน ปฎิทินวิชาการ)</p>
                                <p>6.
                                    เมื่อสํานักส่งเสริมวิชาการและงานทะเบียนอนุมัติการขอยกเลิกรายวิชาเรียนและมีการบันทึกข้อมูลเรียบร้อยแล้วให้นักศึกษาตรวจสอบการ
